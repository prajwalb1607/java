class Switch{
      public static void main(String []args){
	  String fruit="mango";
	  switch(fruit){
	  case "apple":
	               System.out.println("its a red");
				   break;
	  case "orange":
	                System.out.println("its a orange");
					break;
	  case "pinapple":
	                System.out.println("its a pink");
		            break;
	  default :
	                System.out.println("its a red");
					
			}
	}
}